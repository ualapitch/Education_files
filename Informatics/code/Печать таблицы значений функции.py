import math


h = 6.626176*(10**(-34))
k = 1.380662*(10**(-23))
c = 2.99792558*(10**8)
v = 10**13
t = 1000
sp = '   '
print(f'При температуре  {t} К\n\n'
      'выполнены расчеты для заданного интервала частот:\n\n'
      f'Частота  (1/с){sp}|{sp}P  (Дж*с/см3)\n'
      f'{"":-^{len("выполнены расчеты для заданного интервала частот:")}}')
for v in range(10**13, 95*10**12, 5*10**12):
    P = (8*math.pi*h/c**3)*(v**3/(math.exp((h*v)/(k*t)) - 1))
    print(v, P, sep=f'{sp}|{sp}')